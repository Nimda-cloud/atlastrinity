import asyncio
import os
import sys
from unittest.mock import patch, MagicMock, AsyncMock

# Add src to path
sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), "../src")))

async def simulate_tier3_fallback():
    print("üöÄ Starting Live Tier 3 Fallback Simulation")
    
    # We sabotages the environment by removing keys or setting them to invalid values
    # BUT we want to keep the Copilot key if it exists, or at least let it try the proxy.
    
    # In Vibe MCP, we use the library vibe_server directly to invoke the tool logic
    from mcp_server import vibe_server
    
    # We will call vibe_implement_feature
    # It will use devstral-2 (Tier 1) by default
    
    test_prompt = "Create a simple hello world in python"
    
    # Sabotage providers: We mock get_provider to return unavailable for mistral and openrouter
    from mcp_server.vibe_config import ProviderConfig
    
    def mock_get_provider(name):
        if name in ("mistral", "openrouter"):
            p = ProviderConfig(name=name, api_base="http://fail", api_key_env_var=f"FAIL_{name}")
            return p
        # For copilot, we return a working one (or at least valid config)
        if name == "copilot":
            return ProviderConfig(name="copilot", api_base="http://localhost:8085", api_key_env_var="COPILOT_SESSION_TOKEN")
        return None

    print("‚ö†Ô∏è  Sabotaging Tier 1 (Mistral) and Tier 2 (OpenRouter)...")
    
    with patch("mcp_server.vibe_server.get_vibe_config") as mock_conf_getter:
        # We need to return a mock config that uses our mock_get_provider
        from mcp_server.vibe_config import ModelConfig
        
        mock_config = vibe_server.get_vibe_config()
        mock_config.get_provider = mock_get_provider
        
        # Verify raptor-mini is there
        copilot_model = mock_config.get_model_by_alias("gpt-4o")
        print(f"üì° Copilot Model from config: {copilot_model.name} (Alias: {copilot_model.alias})")
        
        mock_conf_getter.return_value = mock_config
        
        # Now we invoke the tool. We expect it to:
        # 1. Try Mistral -> Fail (due to invalid setup/mock)
        # 2. Check OpenRouter -> See it's unavailable (via mock_get_provider)
        # 3. Fallback to Copilot -> Trigger gpt-4o alias -> Which points to raptor-mini
        
        # Mock Context
        mock_ctx = MagicMock()
        mock_ctx.log = AsyncMock()
        
        print("üõ†Ô∏è  Invoking vibe_implement_feature...")
        try:
            # We use a very low timeout to not hang if proxy isn't responding
            result = await vibe_server.vibe_implement_feature(
                ctx=mock_ctx,
                goal=test_prompt,
                cwd=os.getcwd()
            )
            print("\n‚úÖ Simulation Call Finished.")
            print(f"Result success: {result.get('success')}")
        except Exception as e:
            print(f"‚ùå Simulation Call Failed: {e}")

if __name__ == "__main__":
    asyncio.run(simulate_tier3_fallback())
